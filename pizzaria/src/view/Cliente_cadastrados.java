        String id = String.valueOf(tabela.getValueAt(linha, 4));

        campo_nome.setText(nome);
        campo_endereco.setText(endereco);
        campo_cpf.setText(cpf);
        campo_telefone.setText(telefone);
        campo_id.setText(id);
