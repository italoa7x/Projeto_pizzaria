    private void preenchertabela() {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        try {
            PizzaDTO pizzaSelecionada = pizzaControl.listarPizza();
            
            for (PizzaDTO x : pizzaSelecionada.getPizzasCadastradas()) {
                String[] dados = new String[5];
                dados[0] = x.getNome();
                dados[1] = x.getTamanho();
                dados[2] = x.getQuant_fatias()+"";
                dados[3] = x.getValor()+"";
                dados[4] = x.getId() + "";
                model.addRow(dados);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

