    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        try {
            String sabor1 = comboS1.getSelectedItem().toString();
            String sabor2 = comboS2.getSelectedItem().toString();

            if (lblQuantFatias.getText().length() > 0 && sabor1.length() > 0 && sabor2.length() > 0) {

                String[] sabores = {sabor1, sabor2};
                String nome = lblNomePizza.getText();
                double valor = Double.parseDouble((String) txtValorPizza.getText());
                int quant_fatias = Integer.parseInt((String) lblQuantFatias.getText());

                PizzaDTO pizzaDto = new PizzaDTO();
                pizzaDto.setNome(nome);
                pizzaDto.setValor(valor);
                pizzaDto.setQuant_fatias(quant_fatias);
                pizzaDto.setSabores(sabores);
                pizzaDto.setTamanho("Personalizada");
                pizzaDto.setFatia(0);

                // RETORNA O ID DA PIZZA QUE FOI CADASTRADA.
                int id = pizzaControl.salvaPizzaPersonalizada(pizzaDto);
                if (id != 0) {
                    this.tela.setaDadosPizza(nome, valor + "", quant_fatias + "", id + "", "Personalizada");
                    dispose();
                }

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro." + e.getMessage());
        }

    }//GEN-LAST:event_btSalvarActionPerformed


