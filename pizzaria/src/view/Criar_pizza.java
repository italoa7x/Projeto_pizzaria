    private void lblQuantFatiasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lblQuantFatiasKeyReleased
        try {
            if (lblQuantFatias.getText().length() > 0) {
                int quantFatias = Integer.parseInt((String) lblQuantFatias.getText());
                double valorPizza = ((quantFatias * 2) * 2) + 20;
                txtValorPizza.setText(String.valueOf(valorPizza));
            }
            if (lblQuantFatias.getText().length() == 0) {
                txtValorPizza.setText("Valor R$");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Digite apenas nÃºmero.");
        }


    }//GEN-LAST:event_lblQuantFatiasKeyReleased

