    private void calculaTotal() {
        double total = 0;
        if (tabela.getRowCount() > 0) {
            for (int i = 0; i < tabela.getRowCount(); i++) {
                total += Double.parseDouble((String) tabela.getValueAt(i, 3));
            }
            txtTotal.setText("Total R$: " + total + "");
        }

    }

