    private void btEscolherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEscolherActionPerformed
        // TODO add your handling code here:
        int linha = tabela.getSelectedRow();
        if (linha != -1) {
            String nome = (String) tabela.getValueAt(linha, 0);
            String tamanho = (String) tabela.getValueAt(linha, 1);
            String valor = (String) tabela.getValueAt(linha, 2);
            String quant_fatias = (String) tabela.getValueAt(linha, 3);
            String id = (String) tabela.getValueAt(linha, 4);
            tela.setaDadosPizza(nome, valor, quant_fatias, id, tamanho);
            if (nome.length() > 0 && tamanho.length() > 0 && id.length() > 0) {
                dispose();
            }
        }
    }//GEN-LAST:event_btEscolherActionPerformed
    private void btVerSaboresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVerSaboresActionPerformed
        ArrayList<String> sabores = new ArrayList<String>();
        try {
            int linha = tabela.getSelectedRow();
            if (linha != -1) {
                int id = Integer.parseInt((String) tabela.getValueAt(linha, 4));
                sabores = pizzaControl.verSabores(id);
                JOptionPane.showMessageDialog(null, sabores.toArray());
               
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btVerSaboresActionPerformed

