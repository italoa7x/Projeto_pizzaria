    public Inicial(String logado) {
        initComponents();

        verificaFuncionario(logado);
        setaData();
        fLogado = logado;
        lblFuncionarioLogado.setText(logado);
        iniciarHora();
    }

    private void iniciarHora() {
        Thread relogio = new Thread(new Inicial.horaThread());
        relogio.start();
    }

    private void setaData() {
        SimpleDateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
        Date dataAtual = new Date(System.currentTimeMillis());
        lblData.setText("Data \n" + formatador.format(dataAtual));

    }

    private void verificaFuncionario(String cargo) {
        if (cargo.equalsIgnoreCase("atendente")) {
            this.barra_menu.setVisible(false);
            btpedidosAentregar.setEnabled(false);
        } else if (cargo.equalsIgnoreCase("pizzaiolo")) {
            this.barra_menu.setVisible(false);
            btCadastrarCliente.setEnabled(false);
            btNovoPedid.setEnabled(false);
            btpedidosAentregar.setEnabled(false);

        } else if (cargo.equalsIgnoreCase("motoboy")) {
            this.barra_menu.setVisible(false);
            btPedidos.setEnabled(false);
            btCadastrarCliente.setEnabled(false);
            btNovoPedid.setEnabled(false);
        }
    }

    class horaThread implements Runnable {

        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");

        @Override
        public void run() {
            while (true) {
                lblHora.setText("Hora: " + sdf.format(new java.util.Date()));
                try {
                    Thread.sleep(1000);
                } catch (InterruptedException e) {
                    JOptionPane.showMessageDialog(null, "Erro ao exibir hora");
                }
            }

        }

    }

