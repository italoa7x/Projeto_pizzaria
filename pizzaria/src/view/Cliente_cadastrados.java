    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        try {
            String nome = campo_nome.getText();
            String cpf = campo_cpf.getText();
            String telefone = campo_telefone.getText();
            String endereco = campo_endereco.getText();
            int id = Integer.parseInt((String) campo_id.getText());

            ClienteDTO cliDTO = new ClienteDTO();
            cliDTO.setNome(nome);
            cliDTO.setCpf(cpf);
            cliDTO.setEndereco(endereco);
            cliDTO.setTelefone(telefone);
            cliDTO.setId(id);
            
            
            if (clienteControl.atualizarCliente(cliDTO)) {
                JOptionPane.showMessageDialog(null, "Dados atualizados.");
                limpaCampos();
                atualizarTabela();
                desabilitaCampos();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        int linha = tabela.getSelectedRow();
        try {
            if (linha != -1) {
                int idCli = Integer.parseInt((String) campo_id.getText());
                if (clienteControl.excluir(idCli)) {
                    JOptionPane.showMessageDialog(null, "Cliente excluído.");
                    limpaCampos();
                    atualizarTabela();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_btExcluirActionPerformed
    private void limpaCampos() {
        // limpa os campos
        campo_cpf.setText("");
        campo_nome.setText("");
        campo_endereco.setText("");
        campo_telefone.setText("");
        campo_id.setText("");
    }

    private void habilitaCampos() {
        campo_nome.setEditable(true);
        campo_endereco.setEditable(true);
        campo_cpf.setEditable(true);
        campo_telefone.setEditable(true);
        btSalvar.setEnabled(true);
    }

    private void desabilitaCampos() {
        campo_nome.setEditable(false);
        campo_endereco.setEditable(false);
        campo_cpf.setEditable(false);
        campo_telefone.setEditable(false);
        btSalvar.setEnabled(false);
    }
