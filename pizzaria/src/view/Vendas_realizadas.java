    private void preencherTabela() {
        try {
            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            PedidoDTO pedidosDTO = pedidoControl.listarPedidos();
            for (PedidoDTO x : pedidosDTO.getPedidosCadastrados()) {
                String[] dados = new String[5];
                dados[0] = x.getCliente().getNome();
                dados[1] = x.getPizza().getNome();
                dados[2] = x.getPizza().getTamanho();
                dados[3] = x.getPizza().getValor() + "";
                dados[4] = x.getId() + "";
                model.addRow(dados);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
